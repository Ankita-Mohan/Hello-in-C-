#include <iostream> //pre-processor directive to include iostream

using namespace std;
int main()  //main function with no arguments
{
    int a;
    float b;
    char c;
    double d;
    cout << "Size of char: " << sizeof(c) << " byte" << endl;
    cout << "Size of int: " << sizeof(a) << " bytes" << endl;
    cout << "Size of float: " << sizeof(b) << " bytes" << endl;
    cout << "Size of double: " << sizeof(d) << " bytes" << endl;

    return 0;
}
